package br.com.force.sale.Deposito;

public class Deposito {

    private String codDeposito;
    private String descricaoDeposito;
    private String localizacaoDeposito;
    private String situacaoDeposito;

    public String getCodDeposito() {
        return codDeposito;
    }

    public void setCodDeposito(String codDeposito) {
        this.codDeposito = codDeposito;
    }

    public String getDescricaoDeposito() {
        return descricaoDeposito;
    }

    public void setDescricaoDeposito(String descricaoDeposito) {
        this.descricaoDeposito = descricaoDeposito;
    }

    public String getLocalizacaoDeposito() {
        return localizacaoDeposito;
    }

    public void setLocalizacaoDeposito(String localizacaoDeposito) {
        this.localizacaoDeposito = localizacaoDeposito;
    }

    public String getSituacaoDeposito() {
        return situacaoDeposito;
    }

    public void setSituacaoDeposito(String situacaoDeposito) {
        this.situacaoDeposito = situacaoDeposito;
    }
}
